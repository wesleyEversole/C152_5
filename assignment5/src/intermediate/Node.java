package intermediate;

public class Node {
	private String Name;
	private String Child;

	public Node(String n, String c) {
		Name = n;
		Child = c;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getChild() {
		return Child;
	}

	public void setChild(String child) {
		Child = child;
	}
}
